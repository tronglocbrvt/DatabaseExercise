CREATE VIEW SVDK AS
SELECT SV.MASV
FROM SINHVIEN SV, DANGKY DK
WHERE SV.MASV = DK.MASV AND DK.HOCKY = 1 AND DK.NAM = 2004
GROUP BY SV.MASV
HAVING COUNT(DK.MACD) = 2
SELECT SINHVIEN.*
FROM SINHVIEN
JOIN SVDK
ON SINHVIEN.MASV = SVDK.MASV
