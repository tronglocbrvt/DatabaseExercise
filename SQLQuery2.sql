CREATE VIEW CHUAHOCLAI AS
SELECT SV.MASV
FROM SINHVIEN SV
EXCEPT
SELECT DK.MASV
FROM DANGKY DK
GROUP BY DK.MASV, DK.MACD
HAVING COUNT(DK.DIEM) >= 2

SELECT SV.*
FROM SINHVIEN SV
JOIN CHUAHOCLAI
ON SV.MASV = CHUAHOCLAI.MASV
